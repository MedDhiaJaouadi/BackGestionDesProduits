package tn.essat.model;

public class Test {
	private String gatenumber ;
	private int state ;
	
	public Test() {
		super();
	}

	public Test(String gatenumber, int state) {
		super();
		this.gatenumber = gatenumber;
		this.state = state;
	}

	public String getGatenumber() {
		return gatenumber;
	}

	public void setGatenumber(String gatenumber) {
		this.gatenumber = gatenumber;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}
	
	

}
